function renderNeighborLink(entry, direction) {
  if (!entry) return '<span class="diary-entry-spacer" aria-hidden="true"></span>';

  const icon = direction === "previous" ? '<i class="fas fa-arrow-left"></i>' : '<i class="fas fa-arrow-right"></i>';
  const label = direction === "previous" ? "previous entry" : "next entry";
  const content = direction === "previous" ? `${icon}<span>${label}</span>` : `<span>${label}</span>${icon}`;

  return `<a href="${escapeAttribute(`${entry.slug}.html`)}" class="diary-entry-link ${direction}">${content}</a>`;
}

