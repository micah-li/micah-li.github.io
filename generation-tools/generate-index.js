const fs = require("fs");
const path = require("path");

const rootDir = path.resolve(__dirname, "..");
const postsDir = path.join(rootDir, "posts");
const outputDir = path.join(rootDir, "blog");
const postsIndexFile = path.join(postsDir, "posts.json");
const blogIndexFile = path.join(rootDir, "blog-posts.html");
const legacyPostFile = path.join(rootDir, "blog.html");

const generatedMarker = "<!-- Generated by generate-index.js. Do not edit by hand. -->";

const site = {
  title: "micah li",
  blogTitle: "blog",
  blogSubtitle: "a place for many non-math related things. looking for math? look at my homepage",
  diaryPath: "math-diary.html",
  photosPath: "photos.html",
};

function ensureDirectory(directory) {
  if (!fs.existsSync(directory)) {
    fs.mkdirSync(directory, { recursive: true });
  }
}

function cleanGeneratedPosts() {
  if (!fs.existsSync(outputDir)) return;

  for (const file of fs.readdirSync(outputDir)) {
    const filePath = path.join(outputDir, file);
    if (path.extname(filePath) !== ".html") continue;

    const content = fs.readFileSync(filePath, "utf8");
    if (content.includes(generatedMarker)) {
      fs.unlinkSync(filePath);
    }
  }
}

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function escapeAttribute(value) {
  return escapeHtml(String(value).trim());
}

function stripQuotes(value) {
  const trimmed = String(value).trim();
  const first = trimmed[0];
  const last = trimmed[trimmed.length - 1];

  if ((first === '"' && last === '"') || (first === "'" && last === "'")) {
    return trimmed.slice(1, -1);
  }

  return trimmed;
}

function parseFrontmatterValue(value) {
  const trimmed = value.trim();

  if (trimmed.startsWith("[") && trimmed.endsWith("]")) {
    const inner = trimmed.slice(1, -1).trim();
    if (!inner) return [];
    return inner.split(",").map((item) => stripQuotes(item));
  }

  return stripQuotes(trimmed);
}

function parseFrontmatterBlock(rawFrontmatter) {
  const data = {};

  for (const line of rawFrontmatter.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;

    const match = trimmed.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!match) continue;

    data[match[1]] = parseFrontmatterValue(match[2]);
  }

  return data;
}

function splitFrontmatter(source) {
  const normalized = source.replace(/\r\n/g, "\n");
  const lines = normalized.split("\n");

  if (lines[0]?.trim() !== "---") {
    return { frontmatter: {}, markdown: normalized.trim() };
  }

  const closeIndex = lines.findIndex((line, index) => index > 0 && line.trim() === "---");

  if (closeIndex === -1) {
    return { frontmatter: {}, markdown: normalized.trim() };
  }

  return {
    frontmatter: parseFrontmatterBlock(lines.slice(1, closeIndex).join("\n")),
    markdown: lines.slice(closeIndex + 1).join("\n").trim(),
  };
}

function titleFromSlug(slug) {
  return slug
    .split(/[-_]+/)
    .filter(Boolean)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");
}

function slugify(value) {
  return String(value)
    .toLowerCase()
    .trim()
    .replace(/['"]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function stripMarkdown(markdown) {
  return markdown
    .replace(/```[\s\S]*?```/g, " ")
    .replace(/!\[[^\]]*]\([^)]+\)/g, " ")
    .replace(/\[([^\]]+)]\([^)]+\)/g, "$1")
    .replace(/^#{1,6}\s+/gm, "")
    .replace(/^>\s?/gm, "")
    .replace(/[*_~`#>|-]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function truncateText(text, maxLength) {
  if (text.length <= maxLength) return text;

  const truncated = text.slice(0, maxLength).trim();
  const lastSpace = truncated.lastIndexOf(" ");

  return `${truncated.slice(0, lastSpace > 80 ? lastSpace : maxLength).trim()}...`;
}

function createExcerpt(markdown) {
  return truncateText(stripMarkdown(markdown), 155);
}

function dateSortValue(date) {
  const timestamp = Date.parse(`${date}T00:00:00Z`);
  return Number.isNaN(timestamp) ? 0 : timestamp;
}

function formatDate(date) {
  if (!date) return "";

  const parsed = new Date(`${date}T00:00:00Z`);
  if (Number.isNaN(parsed.getTime())) return date;

  return parsed.toLocaleDateString("en-US", {
    day: "numeric",
    month: "long",
    timeZone: "UTC",
    year: "numeric",
  });
}

function createPlaceholderStore() {
  const values = [];

  return {
    add(value) {
      const token = `\u0000${values.length}\u0000`;
      values.push(value);
      return token;
    },
    restore(value) {
      return value.replace(/\u0000(\d+)\u0000/g, (_, index) => values[Number(index)]);
    },
  };
}

function safeUrl(url, prefix = "") {
  const trimmed = String(url).trim().replace(/^<(.+)>$/, "$1");
  const isUnsafe = /^\s*javascript:/i.test(trimmed);
  const isExternal = /^(https?:|mailto:|tel:|data:)/i.test(trimmed);
  const isAnchored = trimmed.startsWith("#") || trimmed.startsWith("/");

  if (isUnsafe) return "#";
  if (!prefix || isExternal || isAnchored) return trimmed;

  return `${prefix}${trimmed}`;
}

function renderInline(markdown, prefix = "") {
  const placeholders = createPlaceholderStore();

  let rendered = String(markdown).replace(/`([^`]+)`/g, (_, code) => {
    return placeholders.add(`<code>${escapeHtml(code)}</code>`);
  });

  rendered = rendered.replace(/!\[([^\]]*)]\(([^)]+)\)/g, (_, alt, src) => {
    return placeholders.add(`<img src="${escapeAttribute(safeUrl(src, prefix))}" alt="${escapeAttribute(alt)}">`);
  });

  rendered = rendered.replace(/\[([^\]]+)]\(([^)]+)\)/g, (_, label, href) => {
    return placeholders.add(`<a href="${escapeAttribute(safeUrl(href, prefix))}">${escapeHtml(label)}</a>`);
  });

  rendered = escapeHtml(rendered)
    .replace(/\*\*([^*]+)\*\*/g, "<strong>$1</strong>")
    .replace(/__([^_]+)__/g, "<strong>$1</strong>")
    .replace(/\*([^*]+)\*/g, "<em>$1</em>")
    .replace(/_([^_]+)_/g, "<em>$1</em>");

  return placeholders.restore(rendered);
}

function isTableDivider(line) {
  return /^\s*\|?\s*:?-{3,}:?\s*(\|\s*:?-{3,}:?\s*)+\|?\s*$/.test(line);
}

function splitTableRow(line) {
  return line
    .trim()
    .replace(/^\|/, "")
    .replace(/\|$/, "")
    .split("|")
    .map((cell) => cell.trim());
}

function indentationWidth(value) {
  let width = 0;

  for (const character of value) {
    width += character === "\t" ? 4 - (width % 4) : 1;
  }

  return width;
}

function getListItem(line) {
  const match = line.match(/^([ \t]*)([-*+]|\d+\.)(?:[ \t]+(.*)|[ \t]*)$/);

  if (!match) return null;

  return {
    indent: indentationWidth(match[1]),
    text: (match[3] || "").trim(),
    type: /^\d+\.$/.test(match[2]) ? "ol" : "ul",
  };
}

function isBlockStart(lines, index) {
  const line = lines[index] || "";
  const nextLine = lines[index + 1] || "";

  return (
    /^```/.test(line) ||
    /^#{1,6}\s+/.test(line) ||
    /^>\s?/.test(line) ||
    getListItem(line) ||
    /^ {0,3}([-*_])(?:\s*\1){2,}\s*$/.test(line) ||
    (line.includes("|") && isTableDivider(nextLine))
  );
}

function renderListAtIndent(lines, index, indent, type, prefix) {
  const items = [];

  while (index < lines.length) {
    const item = getListItem(lines[index]);
    if (!item || item.indent < indent || (item.indent === indent && item.type !== type)) break;

    if (item.indent > indent) {
      if (!items.length) break;

      const nestedList = renderListAtIndent(lines, index, item.indent, item.type, prefix);
      items[items.length - 1].children.push(nestedList.html);
      index = nestedList.nextIndex;
      continue;
    }

    const itemContent = item.text ? [renderInline(item.text, prefix)] : [];
    const children = [];
    index += 1;

    while (index < lines.length) {
      if (!lines[index].trim()) break;

      const nextItem = getListItem(lines[index]);
      if (nextItem) {
        if (nextItem.indent > item.indent) {
          const nestedList = renderListAtIndent(lines, index, nextItem.indent, nextItem.type, prefix);
          children.push(nestedList.html);
          index = nestedList.nextIndex;
          continue;
        }

        break;
      }

      if (isBlockStart(lines, index)) break;

      const continuationIndent = indentationWidth(lines[index].match(/^[ \t]*/)[0]);
      if (continuationIndent < item.indent) break;

      itemContent.push(renderInline(lines[index].trim(), prefix));
      index += 1;
    }

    items.push({ children, content: itemContent.join(" ") });
  }

  const html = `<${type}>\n${items
    .map((item) => {
      const children = item.children.length ? `\n${item.children.join("\n")}` : "";
      return `  <li>${item.content}${children}</li>`;
    })
    .join("\n")}\n</${type}>`;

  return { html, nextIndex: index };
}

function renderList(lines, index, prefix) {
  const item = getListItem(lines[index]);
  if (!item) return null;

  return renderListAtIndent(lines, index, item.indent, item.type, prefix);
}

function renderTable(lines, prefix) {
  const header = splitTableRow(lines[0]);
  const bodyRows = lines.slice(2).map(splitTableRow);

  return [
    "<table>",
    "  <thead>",
    `    <tr>${header.map((cell) => `<th>${renderInline(cell, prefix)}</th>`).join("")}</tr>`,
    "  </thead>",
    "  <tbody>",
    ...bodyRows.map((row) => `    <tr>${row.map((cell) => `<td>${renderInline(cell, prefix)}</td>`).join("")}</tr>`),
    "  </tbody>",
    "</table>",
  ].join("\n");
}

function createHeadingId(text, usedIds) {
  const base = slugify(text.replace(/`([^`]+)`/g, "$1")) || "section";
  const count = usedIds.get(base) || 0;
  usedIds.set(base, count + 1);

  return count === 0 ? base : `${base}-${count + 1}`;
}

function markdownToHtml(markdown, usedHeadingIds = new Map(), prefix = "") {
  const lines = markdown.replace(/\r\n/g, "\n").split("\n");
  const blocks = [];
  let index = 0;

  while (index < lines.length) {
    const line = lines[index];

    if (!line.trim()) {
      index += 1;
      continue;
    }

    const fenceMatch = line.match(/^```([A-Za-z0-9_-]*)\s*$/);
    if (fenceMatch) {
      const language = fenceMatch[1];
      const codeLines = [];
      index += 1;

      while (index < lines.length && !/^```\s*$/.test(lines[index])) {
        codeLines.push(lines[index]);
        index += 1;
      }

      index += index < lines.length ? 1 : 0;
      const languageClass = language ? ` class="language-${escapeAttribute(language)}"` : "";
      blocks.push(`<pre><code${languageClass}>${escapeHtml(codeLines.join("\n"))}</code></pre>`);
      continue;
    }

    const headingMatch = line.match(/^(#{1,6})\s+(.+)$/);
    if (headingMatch) {
      const level = headingMatch[1].length;
      const text = headingMatch[2].trim();
      const id = createHeadingId(text, usedHeadingIds);
      blocks.push(`<h${level} id="${escapeAttribute(id)}">${renderInline(text, prefix)}</h${level}>`);
      index += 1;
      continue;
    }

    if (/^ {0,3}([-*_])(?:\s*\1){2,}\s*$/.test(line)) {
      blocks.push("<hr>");
      index += 1;
      continue;
    }

    if (line.includes("|") && isTableDivider(lines[index + 1] || "")) {
      const tableLines = [line, lines[index + 1]];
      index += 2;

      while (index < lines.length && lines[index].trim() && lines[index].includes("|")) {
        tableLines.push(lines[index]);
        index += 1;
      }

      blocks.push(renderTable(tableLines, prefix));
      continue;
    }

    if (/^>\s?/.test(line)) {
      const quoteLines = [];

      while (index < lines.length && (/^>\s?/.test(lines[index]) || !lines[index].trim())) {
        quoteLines.push(lines[index].replace(/^>\s?/, ""));
        index += 1;
      }

      blocks.push(`<blockquote>\n${markdownToHtml(quoteLines.join("\n"), usedHeadingIds, prefix)}\n</blockquote>`);
      continue;
    }

    const list = renderList(lines, index, prefix);
    if (list) {
      blocks.push(list.html);
      index = list.nextIndex;
      continue;
    }

    const paragraphLines = [];

    while (index < lines.length && lines[index].trim() && !isBlockStart(lines, index)) {
      paragraphLines.push(lines[index].trim());
      index += 1;
    }

    blocks.push(`<p>${renderInline(paragraphLines.join(" "), prefix)}</p>`);
  }

  return blocks.join("\n");
}

function readPost(file) {
  const filePath = path.join(postsDir, file);
  const slug = slugify(path.basename(file, ".md"));
  const source = fs.readFileSync(filePath, "utf8");
  const { frontmatter, markdown } = splitFrontmatter(source);
  const title = frontmatter.title || titleFromSlug(slug);
  const date = frontmatter.date || "";
  const tags = Array.isArray(frontmatter.tags) ? frontmatter.tags : [];
  const excerpt = frontmatter.excerpt || createExcerpt(markdown);

  return {
    date,
    excerpt,
    formattedDate: formatDate(date),
    html: markdownToHtml(markdown, new Map(), "../"),
    slug,
    source: `posts/${file}`,
    tags,
    title,
    url: `blog/${slug}.html`,
  };
}

