function renderLegacyRedirect() {
  const body = `
    <main class="container">
        <div class="blog-page">
            <article class="article-content">
                <h1 class="article-title">redirecting...</h1>
                <p class="post-excerpt">
                    This blog now uses static HTML pages. You should be sent to the right place automatically.
                    <a id="fallback-link" href="blog-posts.html">go to the blog index</a>
                </p>
            </article>
        </div>
    </main>
    <script>
        (function () {
            var params = new URLSearchParams(window.location.search);
            var post = params.get("post");
            var slug = post ? post.replace(/[^a-zA-Z0-9_-]/g, "") : "";
            var target = slug ? "blog/" + slug + ".html" : "blog-posts.html";
            var fallback = document.getElementById("fallback-link");
            fallback.href = target;
            fallback.textContent = slug ? "open this post" : "go to the blog index";
            window.location.replace(target);
        }());
    </script>`;

  return renderShell({
    body,
    description: "Compatibility redirect for older blog links.",
    title: `${site.title} - blog redirect`,
  });
}

function buildBlog() {
  console.log("Building blog from Markdown posts...");

  if (!fs.existsSync(postsDir)) {
    console.error(`Error: posts directory not found at ${postsDir}`);
    process.exit(1);
  }

  ensureDirectory(outputDir);
  cleanGeneratedPosts();

  const files = fs
    .readdirSync(postsDir)
    .filter((file) => path.extname(file) === ".md")
    .sort();

  const posts = files.map(readPost).sort((a, b) => dateSortValue(b.date) - dateSortValue(a.date));
  const slugs = new Set();

  for (const post of posts) {
    if (slugs.has(post.slug)) {
      console.error(`Error: duplicate post slug "${post.slug}"`);
      process.exit(1);
    }
    slugs.add(post.slug);
  }

  for (const post of posts) {
    const outputFile = path.join(outputDir, `${post.slug}.html`);
    fs.writeFileSync(outputFile, renderPostPage(post), "utf8");
  }

  const publicPosts = posts.map(({ html, ...post }) => post);

  fs.writeFileSync(postsIndexFile, JSON.stringify(publicPosts, null, 2), "utf8");
  fs.writeFileSync(blogIndexFile, renderBlogIndex(publicPosts), "utf8");
  fs.writeFileSync(legacyPostFile, renderLegacyRedirect(), "utf8");

  console.log(`Built ${posts.length} post page(s).`);
  console.log(`Updated ${path.relative(rootDir, blogIndexFile)} and ${path.relative(rootDir, postsIndexFile)}.`);
}

buildBlog();
